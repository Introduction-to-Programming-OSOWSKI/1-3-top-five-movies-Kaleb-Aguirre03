#Define the Function topFiveMovies
def topFiveMovies(a, b, c, d, e):
    
    #return five different movies with a 
    #space between each one
    return a + " " + b + " " + c + " " + d + " " + e

#print the funtion with the variables as strings
print (topFiveMovies("a", "b", "c", "d", "e"))

#print the function with five movies
print (topFiveMovies ("Home Alone 2", 
"Spider-Man: Into the Spider-Verse", 
"Dragonball Super: Broly", "Dragonball Z: Fusion Reborn", 
"The Amazing Spider-Man 2"))

